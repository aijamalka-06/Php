<?php

$nums = [1, 2, 3, 1, 4, 5, 3, 2, 6, 7, 7, 8, 8, 9, 2, 5];

/*
 * 1. Сумма элементов массива
 */

/* Вариант 1 — с функцией */
$sum1 = array_sum($nums);

echo "Сумма (с функцией): " . $sum1 . "<br>";

/* Вариант 2 — без функции */
$sum2 = 0;

foreach ($nums as $num) {
    $sum2 += $num;
}

echo "Сумма (без функции): " . $sum2 . "<br><br>";



/*
 * 2. Количество четных чисел
 */

/* Вариант 1 — с функцией */
$evenCount1 = 0;

foreach ($nums as $num) {
    if ($num % 2 == 0) {
        $evenCount1++;
    }
}

echo "Количество четных (с функцией): " . $evenCount1 . "<br>";



/* Вариант 2 — без специальной функции */
$evenCount2 = 0;

for ($i = 0; $i < count($nums); $i++) {
    if ($nums[$i] % 2 == 0) {
        $evenCount2++;
    }
}

echo "Количество четных (без функции): " . $evenCount2;

?>